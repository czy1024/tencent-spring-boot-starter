/**
 * @author luna
 * 2021/6/14
 */
public class LocationDTO {

    /** 纬度 */
    private Double lat;

    /** 经度 */
    private Double lng;

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLng() {
        return lng;
    }

    public void setLng(Double lng) {
        this.lng = lng;
    }
}
